    private void quitarcapaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_quitarcapaActionPerformed

        int[] removeindices = capascargadas.getSelectedIndices();
        Object[] removeseleccionname = capascargadas.getSelectedValues();


        for (int i = 0; i < removeindices.length; i++) {

            indicesselec.remove(removeindices[i]);
            namesselec.remove(removeseleccionname[i]);
        };

        Object[] seleccionnames = namesselec.toArray(new Object[namesselec.size()]);
        capascargadas.setListData(seleccionnames);

    }//GEN-LAST:event_quitarcapaActionPerformed

    private void subircapaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subircapaActionPerformed

        int orderindices = capascargadas.getSelectedIndex();

        if (orderindices != 0) {

            String actual = (String) capascargadas.getSelectedValue();
            String last = namesselec.get(orderindices - 1);

            namesselec.set((orderindices - 1), actual);
            namesselec.set((orderindices), last);

            Object[] seleccionnames = namesselec.toArray(new Object[namesselec.size()]);
            capascargadas.setListData(seleccionnames);
        }

    }//GEN-LAST:event_subircapaActionPerformed

    private void bajarcapaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bajarcapaActionPerformed

        int orderindices = capascargadas.getSelectedIndex();

        if (orderindices != (namesselec.size() - 1)) {

            String actual = (String) capascargadas.getSelectedValue();
            String next = namesselec.get(orderindices + 1);

            namesselec.set((orderindices + 1), actual);
            namesselec.set((orderindices), next);

            Object[] seleccionnames = namesselec.toArray(new Object[namesselec.size()]);
            capascargadas.setListData(seleccionnames);

        }

    }//GEN-LAST:event_bajarcapaActionPerformed

