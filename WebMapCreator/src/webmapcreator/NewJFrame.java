    private void BordeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_BordeItemStateChanged

        if (Borde.isSelected() == true) {

            Grosorborde.setEnabled(true);
            elegircolor.setEnabled(true);
            m.setgrosor(marco[0]);
            Grosorborde.setText(marco[0]);

        } else {

            Grosorborde.setEnabled(false);
            elegircolor.setEnabled(false);
            m.setgrosor("0");
            Grosorborde.setText(marco[0]);

        }
    }//GEN-LAST:event_BordeItemStateChanged

